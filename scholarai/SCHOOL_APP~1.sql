    'EML-' || LPAD(ROW_NUMBER() OVER (ORDER BY x.student_id) + 5, 3, '0') AS email_id,
    x.student_id,
    x.to_email,
    x.cc_email,
    x.template_id,
    x.subject,
    x.body,
    x.priority,
    x.status,
    'ADM-001',
    CURRENT_TIMESTAMP
FROM (
    SELECT
        s.student_id,
        s.email AS to_email,
        s.guardian_email AS cc_email,
        (SELECT template_id FROM email_templates WHERE template_name = 'Fee Dues Reminder') AS template_id,
        'Fee Dues Reminder' AS subject,
        'This is a demo sent email about pending fee dues.' AS body,
        'normal' AS priority,
        'sent' AS status,
        ROW_NUMBER() OVER (ORDER BY s.student_id) AS rn
    FROM students s
    WHERE s.due_amount > 0
) x
WHERE x.rn <= 5;

