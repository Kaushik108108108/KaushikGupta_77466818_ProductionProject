CREATE TABLE admin_users (
    admin_id         VARCHAR2(20) PRIMARY KEY,
    username         VARCHAR2(100) NOT NULL UNIQUE,
    password_hash    VARCHAR2(255) NOT NULL,
    full_name        VARCHAR2(150),
    email            VARCHAR2(150) UNIQUE,
    role_name        VARCHAR2(50) DEFAULT 'Admin' NOT NULL,
    is_active        CHAR(1) DEFAULT 'Y' NOT NULL,
    last_login_at    TIMESTAMP,
    created_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT ck_admin_users_active CHECK (is_active IN ('Y','N'))
);

CREATE TABLE students (
    student_id         VARCHAR2(20) PRIMARY KEY,
    full_name          VARCHAR2(150) NOT NULL,
    email              VARCHAR2(150) NOT NULL UNIQUE,
    password_hash      VARCHAR2(255) NOT NULL,
    class_level        VARCHAR2(20) NOT NULL,
    section            VARCHAR2(10) NOT NULL,
    guardian_name      VARCHAR2(150),
    guardian_email     VARCHAR2(150),
    phone_number       VARCHAR2(30),

    performance_index  NUMBER(5,2) DEFAULT 70 NOT NULL,
    risk_level         VARCHAR2(10) DEFAULT 'low' NOT NULL,
    attendance_rate    NUMBER(5,2) DEFAULT 75 NOT NULL,
    due_amount         NUMBER(10,2) DEFAULT 0 NOT NULL,
    complaint_count    NUMBER(5) DEFAULT 0 NOT NULL,
    confidence_score   NUMBER(5,2) DEFAULT 75,

    trend              VARCHAR2(20) DEFAULT 'stable',
    trend_label        VARCHAR2(50),
    trend_note         VARCHAR2(500),

    account_status     VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,

    last_login_at      TIMESTAMP,
    created_at         TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at         TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE subjects (
    subject_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subject_code    VARCHAR2(20) UNIQUE,
    subject_name    VARCHAR2(100) NOT NULL UNIQUE,
    is_active       CHAR(1) DEFAULT 'Y' NOT NULL,
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT ck_subjects_active CHECK (is_active IN ('Y','N'))
);

CREATE TABLE student_academic_records (
    record_id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id          VARCHAR2(20) NOT NULL,
    subject_id          NUMBER NOT NULL,
    academic_year       NUMBER(4) NOT NULL,
    attendance_rate     NUMBER(5,2) DEFAULT 0 NOT NULL,
    term1_score         NUMBER(5,2) DEFAULT 0 NOT NULL,
    term2_score         NUMBER(5,2) DEFAULT 0 NOT NULL,
    term3_score         NUMBER(5,2) DEFAULT 0 NOT NULL,
    predicted_score     NUMBER(5,2),
    grade               VARCHAR2(5),
    trend               VARCHAR2(20),
    trend_label         VARCHAR2(50),
    ai_recommendation   CLOB,
    last_evaluated_at   TIMESTAMP,
    created_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_sar_student FOREIGN KEY (student_id) REFERENCES students(student_id),
    CONSTRAINT fk_sar_subject FOREIGN KEY (subject_id) REFERENCES subjects(subject_id),
    CONSTRAINT uq_sar UNIQUE (student_id, subject_id, academic_year),
    CONSTRAINT ck_sar_att CHECK (attendance_rate BETWEEN 0 AND 100),
    CONSTRAINT ck_sar_t1 CHECK (term1_score BETWEEN 0 AND 100),
    CONSTRAINT ck_sar_t2 CHECK (term2_score BETWEEN 0 AND 100),
    CONSTRAINT ck_sar_t3 CHECK (term3_score BETWEEN 0 AND 100),
    CONSTRAINT ck_sar_pred CHECK (predicted_score IS NULL OR predicted_score BETWEEN 0 AND 100),
    CONSTRAINT ck_sar_trend CHECK (trend IS NULL OR trend IN ('improving','declining','unstable','recovering','stable'))
);

CREATE TABLE complaints (
    complaint_id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id           VARCHAR2(20) NOT NULL,
    complaint_type       VARCHAR2(30) NOT NULL,
    description          CLOB,
    severity             VARCHAR2(10) DEFAULT 'MEDIUM' NOT NULL,
    status               VARCHAR2(20) DEFAULT 'OPEN' NOT NULL,
    recorded_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    recorded_by_admin_id VARCHAR2(20),
    resolved_at          TIMESTAMP,
    CONSTRAINT fk_complaints_student FOREIGN KEY (student_id) REFERENCES students(student_id),
    CONSTRAINT fk_complaints_admin FOREIGN KEY (recorded_by_admin_id) REFERENCES admin_users(admin_id),
    CONSTRAINT ck_complaint_type CHECK (complaint_type IN ('DISCIPLINARY','ATTENDANCE','ACADEMIC','FINANCE','OTHER')),
    CONSTRAINT ck_complaint_severity CHECK (severity IN ('LOW','MEDIUM','HIGH')),
    CONSTRAINT ck_complaint_status CHECK (status IN ('OPEN','UNDER_REVIEW','RESOLVED','DISMISSED'))
);

CREATE TABLE student_fee_dues (
    fee_due_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id        VARCHAR2(20) NOT NULL,
    term_label        VARCHAR2(50),
    due_title         VARCHAR2(150) NOT NULL,
    amount_due        NUMBER(10,2) NOT NULL,
    amount_paid       NUMBER(10,2) DEFAULT 0 NOT NULL,
    due_date          DATE,
    status            VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    notes             VARCHAR2(500),
    created_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_fee_student FOREIGN KEY (student_id) REFERENCES students(student_id),
    CONSTRAINT ck_fee_due CHECK (amount_due >= 0),
    CONSTRAINT ck_fee_paid CHECK (amount_paid >= 0),
    CONSTRAINT ck_fee_status CHECK (status IN ('PENDING','PARTIAL','PAID','WAIVED','OVERDUE'))
);

CREATE TABLE predictions (
    prediction_id             VARCHAR2(20) PRIMARY KEY,
    student_id                VARCHAR2(20) NOT NULL,
    subject_id                NUMBER NOT NULL,
    subject_name_snapshot     VARCHAR2(100) NOT NULL,
    full_name_snapshot        VARCHAR2(150) NOT NULL,
    class_level_snapshot      VARCHAR2(20) NOT NULL,
    term1_score               NUMBER(5,2) DEFAULT 0 NOT NULL,
    term2_score               NUMBER(5,2) DEFAULT 0 NOT NULL,
    term3_score               NUMBER(5,2) DEFAULT 0 NOT NULL,
    attendance_rate           NUMBER(5,2) DEFAULT 0 NOT NULL,
    complaint_count_snapshot  NUMBER(5) DEFAULT 0 NOT NULL,
    due_amount_snapshot       NUMBER(10,2) DEFAULT 0 NOT NULL,
    predicted_score           NUMBER(5,2) NOT NULL,
    risk_level                VARCHAR2(10) NOT NULL,
    trend                     VARCHAR2(20),
    trend_label               VARCHAR2(50),
    trend_note                VARCHAR2(500),
    grade                     VARCHAR2(5),
    performance_index_label   VARCHAR2(50),
    confidence_score          NUMBER(5,2),
    ai_recommendation         CLOB,
    predicted_by_role         VARCHAR2(20) NOT NULL,
    predicted_by_admin_id     VARCHAR2(20),
    created_at                TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_predictions_student FOREIGN KEY (student_id) REFERENCES students(student_id),
    CONSTRAINT fk_predictions_subject FOREIGN KEY (subject_id) REFERENCES subjects(subject_id),
    CONSTRAINT fk_predictions_admin FOREIGN KEY (predicted_by_admin_id) REFERENCES admin_users(admin_id),
    CONSTRAINT ck_predictions_t1 CHECK (term1_score BETWEEN 0 AND 100),
    CONSTRAINT ck_predictions_t2 CHECK (term2_score BETWEEN 0 AND 100),
    CONSTRAINT ck_predictions_t3 CHECK (term3_score BETWEEN 0 AND 100),
    CONSTRAINT ck_predictions_att CHECK (attendance_rate BETWEEN 0 AND 100),
    CONSTRAINT ck_predictions_due CHECK (due_amount_snapshot >= 0),
    CONSTRAINT ck_predictions_pred CHECK (predicted_score BETWEEN 0 AND 100),
    CONSTRAINT ck_predictions_conf CHECK (confidence_score IS NULL OR confidence_score BETWEEN 0 AND 100),
    CONSTRAINT ck_predictions_risk CHECK (risk_level IN ('low','medium','high')),
    CONSTRAINT ck_predictions_trend CHECK (trend IS NULL OR trend IN ('improving','declining','unstable','recovering','stable')),
    CONSTRAINT ck_predicted_by_role CHECK (predicted_by_role IN ('Admin','Student','System'))
);

CREATE TABLE prediction_flags (
    flag_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    prediction_id    VARCHAR2(20) NOT NULL,
    flag_name        VARCHAR2(100) NOT NULL,
    created_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_prediction_flags_prediction FOREIGN KEY (prediction_id)
        REFERENCES predictions(prediction_id) ON DELETE CASCADE,
    CONSTRAINT uq_prediction_flag UNIQUE (prediction_id, flag_name)
);

CREATE TABLE ai_recommendations (
    recommendation_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id          VARCHAR2(20) NOT NULL,
    subject_id          NUMBER,
    recommendation_text CLOB NOT NULL,
    rec_type            VARCHAR2(20) DEFAULT 'info' NOT NULL,
    source_type         VARCHAR2(20) DEFAULT 'SYSTEM' NOT NULL,
    is_active           CHAR(1) DEFAULT 'Y' NOT NULL,
    created_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_ai_rec_student FOREIGN KEY (student_id) REFERENCES students(student_id),
    CONSTRAINT fk_ai_rec_subject FOREIGN KEY (subject_id) REFERENCES subjects(subject_id),
    CONSTRAINT ck_ai_rec_type CHECK (rec_type IN ('success','warning','danger','info')),
    CONSTRAINT ck_ai_rec_source CHECK (source_type IN ('SYSTEM','ADMIN','MODEL')),
    CONSTRAINT ck_ai_rec_active CHECK (is_active IN ('Y','N'))
);

CREATE TABLE email_templates (
    template_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    template_name      VARCHAR2(100) NOT NULL UNIQUE,
    default_subject    VARCHAR2(300),
    default_body       CLOB,
    is_active          CHAR(1) DEFAULT 'Y' NOT NULL,
    created_at         TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT ck_email_templates_active CHECK (is_active IN ('Y','N'))
);

CREATE TABLE email_logs (
    email_id              VARCHAR2(20) PRIMARY KEY,
    student_id            VARCHAR2(20) NOT NULL,
    to_email              VARCHAR2(150) NOT NULL,
    cc_email              VARCHAR2(150),
    template_id           NUMBER,
    subject               VARCHAR2(300) NOT NULL,
    body                  CLOB NOT NULL,
    priority              VARCHAR2(10) DEFAULT 'normal' NOT NULL,
    status                VARCHAR2(10) DEFAULT 'draft' NOT NULL,
    created_by_admin_id   VARCHAR2(20) NOT NULL,
    sent_at               TIMESTAMP,
    created_at            TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at            TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_email_student FOREIGN KEY (student_id) REFERENCES students(student_id),
    CONSTRAINT fk_email_template FOREIGN KEY (template_id) REFERENCES email_templates(template_id),
    CONSTRAINT fk_email_admin FOREIGN KEY (created_by_admin_id) REFERENCES admin_users(admin_id),
    CONSTRAINT ck_email_priority CHECK (priority IN ('normal','high','urgent')),
    CONSTRAINT ck_email_status CHECK (status IN ('draft','sent','failed'))
);

CREATE TABLE reports (
    report_id              VARCHAR2(20) PRIMARY KEY,
    title                  VARCHAR2(200) NOT NULL,
    report_type            VARCHAR2(50) NOT NULL,
    generated_by_type      VARCHAR2(20) DEFAULT 'ADMIN' NOT NULL,
    generated_by_admin_id  VARCHAR2(20),
    status                 VARCHAR2(20) DEFAULT 'DRAFT' NOT NULL,
    file_path              VARCHAR2(500),
    description            CLOB,
    created_at             TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_reports_admin FOREIGN KEY (generated_by_admin_id) REFERENCES admin_users(admin_id),
    CONSTRAINT ck_reports_generated_by CHECK (generated_by_type IN ('ADMIN','SYSTEM')),
    CONSTRAINT ck_reports_status CHECK (status IN ('DRAFT','PUBLISHED','REVIEW','ESCALATED','ARCHIVED'))
);

CREATE TABLE chat_sessions (
    session_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    owner_role       VARCHAR2(20) NOT NULL,
    admin_id         VARCHAR2(20),
    student_id       VARCHAR2(20),
    session_label    VARCHAR2(200) NOT NULL,
    is_active        CHAR(1) DEFAULT 'Y' NOT NULL,
    created_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_chat_sessions_admin FOREIGN KEY (admin_id) REFERENCES admin_users(admin_id),
    CONSTRAINT fk_chat_sessions_student FOREIGN KEY (student_id) REFERENCES students(student_id),
    CONSTRAINT ck_chat_sessions_role CHECK (owner_role IN ('ADMIN','STUDENT')),
    CONSTRAINT ck_chat_sessions_active CHECK (is_active IN ('Y','N')),
    CONSTRAINT ck_chat_sessions_owner CHECK (
        (owner_role = 'ADMIN' AND admin_id IS NOT NULL AND student_id IS NULL)
        OR
        (owner_role = 'STUDENT' AND student_id IS NOT NULL AND admin_id IS NULL)
    )
);

CREATE TABLE chat_messages (
    message_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id       NUMBER NOT NULL,
    sender_type      VARCHAR2(20) NOT NULL,
    message_text     CLOB NOT NULL,
    created_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_chat_messages_session FOREIGN KEY (session_id)
        REFERENCES chat_sessions(session_id) ON DELETE CASCADE,
    CONSTRAINT ck_chat_messages_sender CHECK (sender_type IN ('USER','BOT','SYSTEM'))
);

