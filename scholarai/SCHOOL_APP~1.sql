DECLARE
    PROCEDURE drop_table_safe(p_table_name VARCHAR2) IS
    BEGIN
        EXECUTE IMMEDIATE 'DROP TABLE ' || p_table_name || ' CASCADE CONSTRAINTS PURGE';
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE != -942 THEN
                RAISE;
            END IF;
    END;
    

    PROCEDURE drop_sequence_safe(p_sequence_name VARCHAR2) IS
    BEGIN
        EXECUTE IMMEDIATE 'DROP SEQUENCE ' || p_sequence_name;
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE != -2289 THEN
                RAISE;
            END IF;
    END;

BEGIN
    drop_table_safe('PREDICTION_FLAGS');
    drop_table_safe('CHAT_MESSAGES');
    drop_table_safe('EMAIL_LOGS');
    drop_table_safe('REPORTS');
    drop_table_safe('PREDICTIONS');
    drop_table_safe('AI_RECOMMENDATIONS');
    drop_table_safe('STUDENT_FEE_DUES');
    drop_table_safe('COMPLAINTS');
    drop_table_safe('STUDENT_ACADEMIC_RECORDS');
    drop_table_safe('CHAT_SESSIONS');
    drop_table_safe('EMAIL_TEMPLATES');
    drop_table_safe('SUBJECTS');
    drop_table_safe('STUDENTS');
    drop_table_safe('ADMIN_USERS');

    drop_sequence_safe('SEQ_ADMIN_USERS');
    drop_sequence_safe('SEQ_STUDENTS');
    drop_sequence_safe('SEQ_PREDICTIONS');
    drop_sequence_safe('SEQ_EMAIL_LOGS');
    drop_sequence_safe('SEQ_REPORTS');
END;
