INSERT INTO student_academic_records (
    student_id,
    subject_id,
    academic_year,
    attendance_rate,
    term1_score,
    term2_score,
    term3_score,
    predicted_score,
    grade,
    trend,
    trend_label,
    ai_recommendation,
    last_evaluated_at
)
SELECT
    x.student_id,
    x.subject_id,
    2026,
    x.attendance_rate,
    x.term1_score,
    x.term2_score,
    x.term3_score,
    x.predicted_score,
    CASE
        WHEN x.predicted_score >= 80 THEN 'A'
        WHEN x.predicted_score >= 65 THEN 'B'
        WHEN x.predicted_score >= 50 THEN 'C'
        ELSE 'D'
    END AS grade,
    x.trend,
    CASE
        WHEN x.trend = 'improving' THEN 'Improving'
        WHEN x.trend = 'declining' THEN 'Declining'
        ELSE 'Stable'
    END AS trend_label,
    CASE
        WHEN x.predicted_score < 50 THEN 'Arrange remedial support and weekly follow-up.'
        WHEN x.predicted_score < 65 THEN 'Needs moderate support and more revision practice.'
        ELSE 'Maintain progress with regular study schedule.'
    END AS ai_recommendation,
    CURRENT_TIMESTAMP
FROM (
    SELECT
        s.student_id,
        sub.subject_id,
        LEAST(100, GREATEST(40, s.attendance_rate + MOD(sub.subject_id * 2, 7) - 3)) AS attendance_rate,
        45 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) + sub.subject_id * 3, 41) AS term1_score,
        48 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) * 2 + sub.subject_id * 5, 38) AS term2_score,
        50 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) * 3 + sub.subject_id * 7, 36) AS term3_score,
        ROUND((
            (45 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) + sub.subject_id * 3, 41)) +
            (48 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) * 2 + sub.subject_id * 5, 38)) +
            (50 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) * 3 + sub.subject_id * 7, 36))
        ) / 3, 2) AS predicted_score,
        CASE
            WHEN (50 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) * 3 + sub.subject_id * 7, 36))
               - (45 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) + sub.subject_id * 3, 41)) >= 8
            THEN 'improving'
            WHEN (45 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) + sub.subject_id * 3, 41))
               - (50 + MOD(TO_NUMBER(SUBSTR(s.student_id, 5)) * 3 + sub.subject_id * 7, 36)) >= 8
            THEN 'declining'
            ELSE 'stable'
        END AS trend
    FROM students s
    CROSS JOIN subjects sub
) x;

DECLARE
    v_type      VARCHAR2(30);
    v_severity  VARCHAR2(10);
BEGIN
    FOR s IN (
        SELECT student_id, complaint_count
        FROM students
        WHERE complaint_count > 0
    ) LOOP
        FOR i IN 1 .. s.complaint_count LOOP
            v_type := CASE MOD(i + TO_NUMBER(SUBSTR(s.student_id, 5)), 4)
                        WHEN 0 THEN 'DISCIPLINARY'
                        WHEN 1 THEN 'ATTENDANCE'
                        WHEN 2 THEN 'ACADEMIC'
                        ELSE 'OTHER'
                      END;

            v_severity := CASE
                            WHEN s.complaint_count >= 3 THEN 'HIGH'
                            WHEN s.complaint_count = 2 THEN 'MEDIUM'
                            ELSE 'LOW'
                          END;

            INSERT INTO complaints (
                student_id,
                complaint_type,
                description,
                severity,
                status,
                recorded_at,
                recorded_by_admin_id
            ) VALUES (
                s.student_id,
                v_type,
                'Seeded complaint record for testing for ' || s.student_id,
                v_severity,
                'OPEN',
                CURRENT_TIMESTAMP - NUMTODSINTERVAL((i * 3), 'DAY'),
                'ADM-001'
            );
        END LOOP;
    END LOOP;
END;
